using System;


namespace Calfunctions
{
    public class Function
    {

        public int Sum(int a, int b)
        {
            return a + b;
        }

        public int Substraction(int a, int b)
        {
            return a - b;
        }

        public int Multiplication(int a, int b)
        {
            return a * b;
        }

        public int Division(int a, int b)
        {
            return a / b;
        }





    }

}
